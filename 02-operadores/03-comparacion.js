let a = 10;

//Operadores relacionales
console.log(a > 5); // igualdad de valor
console.log(a >= 5); // mayor o igual que
console.log(a < 5); // menor que
console.log(a <= 5); // menor o igual que

//Operadores de igualdad
console.log(a == '10'); // igualdad de valor
console.log(a != '10'); // desigualdad de valor
console.log(a == '10'); // igualdad de valor 
console.log(a === '10'); // igualdad de valor y tipo
console.log(a !== '10'); // desigualdad de valor y tipo